package MyIf;

public class MyIf {
    public static void main(String[] args){

        int myInt = 10;
        if(myInt > 20){
            System.out.println("myInt: " + myInt);
        }else if(myInt > 15){
            System.out.println("myInt: " + myInt);
        }else{
            System.out.println("myInt: " + myInt);
        }
    }
}
